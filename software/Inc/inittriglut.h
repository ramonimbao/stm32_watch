int16_t i16_sin(int16_t degrees) {
	while (degrees < 0 || degrees > 359) {
		if (degrees < 0)
			degrees += 360;
		else if (degrees > 359)
			degrees -= 360;
	}
	return SIN_LUT[degrees / 6];
}

int16_t i16_cos(int16_t degrees) {
	while (degrees < 0 || degrees > 359) {
		if (degrees < 0)
			degrees += 360;
		else if (degrees > 359)
			degrees -= 360;
	}
	return COS_LUT[degrees / 6];
}

